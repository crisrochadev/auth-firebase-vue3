<script>
import Swal from 'sweetalert2'
import { facebook, google, github, microsoft, twitter } from '../firebase'
import { useConfig } from "@/stores/config";
export default {
  props: ['session', 'methods'],
  emits: ["update:session"],
  data() {

    const config = useConfig()
    return { config }

  },
  methods: {
    async login(provider) {
      this.config.loading = true
      let res;
      if (provider == 'facebook') {
        res = await facebook()
      }
      if (provider == 'google') {
        res = await google()
      }
      if (provider == 'github') {
        res = await github()
      }
      if (provider == 'microsoft') {
        res = await microsoft()
      }
      if (provider == 'twitter') {
        res = await twitter()
      }
      if (res.success) {
        this.$emit("update:session", {
          ...res.user,
          accessToken: res.credential.accessToken,
          token: res.token
        })
      } else {
        console.error(res)
        Swal.fire({
          icon: "error",
          title: "Oops...",
          text: "NÃ£o foi possivel logar, tente mais tarde",
        });
      }
      this.config.loading = false
    }


  }
}
</script>

